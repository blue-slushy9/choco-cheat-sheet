# This is a cheat sheet for using Chocolatey package manager on Windows;

# Upgrade all choco packages (including chocolatey?);
choco upgrade all

# Search for a package;
choco search <package name or key word>

# Install a package;
choco install <package name>

# Reinstall a package;
choco install --force <package name>

# Uninstall a package;
choco uninstall <package name>

# Upgrade a package;
choco upgrade <package name>

# View all installed choco packages;
choco list